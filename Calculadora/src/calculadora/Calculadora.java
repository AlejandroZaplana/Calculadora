public class Calculadora {

    public static double sumar(double num1, double num2){
        return num1+num2;
    }
 
    public static double multiplicar(double num1,double num2){
        return num1*num2;
    }
    
    public static double divide(double num1,double num2){
        return num1/num2;
    }
    
}
